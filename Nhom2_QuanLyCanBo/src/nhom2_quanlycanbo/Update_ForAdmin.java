    public void setUpdate(KySu s) {
        jTxtID.setText(s.getID() + "");
        jTxtID.setEditable(false);
        jTxtUsername.setText(s.getUsername());
        jTextField1.setText(s.getHoten());
        jComboBox1.setSelectedItem(s.getNamsinh());
        if (s.getGioitinh().equals("Nam")) {
            jRadioButton1.setSelected(true);
        } else {
            jRadioButton2.setSelected(true);
        }
        jTextField3.setText(s.getDiachi());

        if (s.getChucvu().equals("Cán bộ")) {
            jRBtnCanBo.setSelected(true);

        } else if (s.getChucvu().equals("Công nhân")) {
            jRBtnCongNhan.setSelected(true);
            jTxtBac.setText(s.getMota());
        } else if (s.getChucvu().equals("Nhân viên phục vụ")) {
            jRBtnNhanVien.setSelected(true);
            jTxtCv.setText(s.getMota());
        } else {
            jTxtNganhDT.setText(s.getMota());
        }

    }
