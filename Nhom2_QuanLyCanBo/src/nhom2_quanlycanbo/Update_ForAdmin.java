    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String gt = "";
        if (jRadioButton1.isSelected()) {
            gt = "Nam";
        } else {
            gt = "Nữ";
        }
        String cv = "";
        if (jRBtnCanBo.isSelected()) {
            cv = "Cán bộ";
        }
        if (jRBtnCongNhan.isSelected()) {
            cv = "Công nhân (bậc " + jTxtBac.getText() + ")";
        }
        if (jRBtnNhanVien.isSelected()) {
            cv = "Nhân Viên" + jTxtCv.getText();
        }
        if (jRBtnKySu.isSelected()) {
            cv = "Kỹ sư" + jTxtNganhDT.getText();
        }
        if (jTxtUsername.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập Username");
        }
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập họ tên");
        }
        if (jTextField3.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập địa chỉ");
        }
        if (jRBtnCongNhan.isSelected()&&jTxtBac.getText().equals("") ) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập bậc tương ứng");
        }
        if (jRBtnKySu.isSelected()&&jTxtNganhDT.getText().equals("") ) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập ngành đào tạo tương ứng");
        }
        if (jRBtnNhanVien.isSelected()&&jTxtCv.getText().equals("") ) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập công việc tương ứng");
        }
        else {
            try {
                AdminDAO ad = new AdminDAO();
                KySu ks = new KySu();
                ks.setID(Integer.parseInt(jTxtID.getText()));
                ks.setUsername(jTxtUsername.getText());
                ks.setHoten(jTextField1.getText());
                ks.setNamsinh(jComboBox1.getSelectedItem().toString());
                ks.setDiachi(jTextField3.getText());
                ks.setGioitinh(gt);
                ks.setMota(cv);
                ad.updateCv(ks);
                if (ad.updateCv(ks) > 0) {
                    int choose = JOptionPane.showConfirmDialog(rootPane, "Thêm thông tin thành công.\n"
                            + "Bạn có muốn tiếp tục?", "Thông báo", JOptionPane.YES_NO_OPTION);
                    if (choose == JOptionPane.YES_OPTION) {
                        jTxtUsername.setText("");
                        jTxtBac.setText("");
                        jTxtCv.setText("");
                        jTxtID.setText("");
                        jTxtBac.setText("");
                        jTextField1.setText("");
                        jTxtNganhDT.setText("");
                        jTextField3.setText("");
                        jRadioButton1.setSelected(false);
                        jRadioButton2.setSelected(false);
                        jComboBox1.setSelectedItem(false);
                        jRBtnCanBo.setSelected(false);
                        jRBtnKySu.setSelected(false);
                        jRBtnNhanVien.setSelected(false);
                        jRBtnCongNhan.setSelected(false);
                    } else {
                        this.setVisible(false);
                        MainMenuView_Admin admin = new MainMenuView_Admin();
                        admin.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Cập nhật thất bại");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

